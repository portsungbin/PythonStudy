from random import *

print(random())
print(random() * 10) # 0.0 ~ 1.0까지 랜덤
print(int(random() * 10)) #0~10까지 랜덤
print(int(random() * 10)) #0~10까지 랜덤
print(int(random() * 10)) #0~10까지 랜덤
print(int(random() * 10) + 1) #1~10까지 랜덤

print(randrange(1, 46)) # 1~45 미만의 랜덤

print(randint(1, 45)) # 1~45이하의 랜덤